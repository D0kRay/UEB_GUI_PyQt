class DataTypes:

    def __init__(self):
        self.uint8_t = "uint8_t"
        self.uint16_t = "uint16_t"
        self.uint32_t = "uint32_t"
        self.float = "float"
        self.double = "double"
        self.long = "long"
        self.int8_t = "int8_t"
        self.int16_t = "int16_t"
        self.int32_t = "int32_t"
        self.str = "String"
        
    def getDataTypesAsList(self):
        return [self.uint8_t, self.uint16_t, self.uint32_t, self.int8_t, self.int16_t, self.int32_t, self.float, self.double, self.long, self.str]
        
